	<p><strong>Censo Electoral o Padrón Electoral</strong></p>
	<p>El censo electoral o padrón electoral es el documento o registro donde constan el conjunto de personas físicas y/o jurídicas a las que la legislación de cada país o las normas de una institución, reconocen el derecho al sufragio activo para elegir a sus representantes, bien en una institución política, bien en una entidad privada o pública.</p>
	<p>De manera general y común, el término se emplea para fijar las personas físicas que tienen derecho a participar en un proceso electoral concreto de contenido político y que no se encuentren privados de dicho derecho. Es importante que Usted como Socio de la Caja de Ahorro consulte su cedula en el siguiente listado, con el fin de certificar su derecho al voto: <a href="pdf/Padron_Electoral.pdf" target="_blank">Padrón Electoral</a></p>
