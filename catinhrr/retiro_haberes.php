	<p>Los Asociados podrán solicitar Retiros de Haberes bien sea Parcial o Total.</p>
	<table width="570" border="0" cellspacing="0" cellpadding="0">
	<tr><td width="280" align="left" valign="top" class="TextoCuerpo">
	<p><strong>Retiro Parcial</strong></p>
	<p>El Asociado que tenga ms de un (1) año de antiguedad en la asociación puede solicitar su retiro parcial, cuyo monto máximo será igual al 80% de los haberes, una vez deducido el saldo de préstamos pendientes y las fianzas que mantega el asociado con CATINH"RR".</p> 
	</td><td width="10" align="left" valign="top">&nbsp;<br></td>	 
	<td width="280" align="left" valign="top" class="TextoCuerpo">
	<p><strong>Retiro Total</strong></p>
	<p>El retiro total de los haberes sólo se permitirá cuando se pierda legalmente la condición de Asociado, en cuyo caso, se le entregará al Asociado la cantidad líquida que tenga en su haber deducidas las cantidades que adeudare por conceptos de préstamos y demás obligaciones contraídas con CATINH"RR". En caso de muerte del Asociado, la entrega de haberes se realizará a los herederos correspondientes o a quienes hayan sido previamente designados por el Asociado como beneficiarios.</p> 
